                    <p>The club is the most valuable sports team in the world, worth €2.5 billion ($3.4 billion) and the world's
                     highest-earning football club for 2013–14, with an annual revenue of €549.5 million.The club is one 
                     of the most widely supported teams in the world. Real Madrid is one of three founding members of the Primera 
                     División that have never been relegated from the top division, along with Athletic Bilbao and Barcelona. The club 
                     holds many long-standing rivalries, most notably El Clásico with Barcelona and the El Derbi madrileño with Atlético 
                     Madrid.</p>
